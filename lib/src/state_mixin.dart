import 'package:flutter/material.dart';

mixin TextControllerMixin<T extends StatefulWidget> on State<T> {
  late TextEditingController textController;

  @override
  void initState() {
    super.initState();
    textController = TextEditingController();
  }

  @override
  void dispose() {
    textController.dispose();
    super.dispose();
  }
}

